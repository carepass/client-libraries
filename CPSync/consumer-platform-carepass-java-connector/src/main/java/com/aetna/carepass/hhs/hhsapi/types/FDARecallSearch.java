package com.aetna.carepass.hhs.hhsapi.types;

public class FDARecallSearch {

	private String brandName;
	private String company;
	private String companyReleaseLink;
	private String date;
	private String photosLink;
	private String productDescription;
	private String reason;

	public String getBrandName() {
		return brandName;
	}

	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public String getCompanyReleaseLink() {
		return companyReleaseLink;
	}

	public void setCompanyReleaseLink(String companyReleaseLink) {
		this.companyReleaseLink = companyReleaseLink;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getPhotosLink() {
		return photosLink;
	}

	public void setPhotosLink(String photosLink) {
		this.photosLink = photosLink;
	}

	public String getProductDescription() {
		return productDescription;
	}

	public void setProductDescription(String productDescription) {
		this.productDescription = productDescription;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

}
